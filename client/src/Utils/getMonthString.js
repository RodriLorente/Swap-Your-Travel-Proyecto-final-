export const monthString = (n) =>{
  let mes = ""
  if(n === 0){
  mes = "Enero"
  }else if(n === 1){
  mes = 'Febrero'
  }else if(n === 2){
  mes = 'Marzo'
  }else if(n === 3){
  mes = 'Abril'
  }else if(n === 4){
  mes = 'Mayo'
  }else if(n === 5){
  mes = 'Junio'
  }else if(n === 6){
  mes = 'Julio'
  }else if(n === 7){
  mes = 'Agosto'
  }else if(n === 8){
  mes = 'Spetiembre'
  }else if(n === 9){
  mes = 'Octubre'
  }else if(n === 10){
  mes = 'Noviembre'
  }else if(n === 11){
  mes = "Diciembre"
  }
  return mes
}

